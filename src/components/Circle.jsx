import {useCurrentFrame} from 'remotion'

export const Circle = ({position, radius, signal = {}, fill = 'black', strokeWidth = 10, timeFunction = (t) => t}) => {

	const t_ 	  = useCurrentFrame()
	const t 	  = timeFunction(t_)

	// const color = signal.color(t, signal.t0)

	return <circle cx={position[t_].x} cy={position[t_].y} r={radius} fill = {fill} stroke = {fill} strokeWidth = {strokeWidth}/>
}